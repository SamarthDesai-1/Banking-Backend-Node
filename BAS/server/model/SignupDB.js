const mongoose = require("mongoose");

let schema = new mongoose.Schema({
  FirstName: {
    type: String,
    required: true,
  },
  LastName: {
    type: String,
    required: true,
  },
  Email: {
    type: String,
    required: true,
    unique: true,
  },
  Password: {
    type: String,
    required: true,
  },
});

const UserSignupSchema = mongoose.model("user_db", schema);

module.exports = UserSignupSchema;
